#列题1：变量名必须是大小写英文、数字和下划线（_）的组合，且不能用数字开头
#判断列表a里面的每个值是否符合python的变量
import  re
a = ["2name","e_name","45_opp","opoejg","89000","_iu88_","_iu8#8_"]
for i in a:
    #b = re.match(r"[a-zA-Z_]+[0-9]*",i)#这种匹配不对
    #b = re.match(r"[a-zA-Z_][a-zA-Z0-9_]+", i) #无法判断_iu8#8_
    b = re.match(r"^[a-zA-Z_][a-zA-Z0-9_]+$", i)#^开头判断以谁开头$判断结尾
    if b:
        print("%s 符合匹配要求" %i)
    else:
        print("%s不符合匹配要求" %i)

